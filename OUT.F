      SUBROUTINE OUT(IN, VEN, ISTART, ISTOP)
      
      COMMON /INIT/ NR, NW, DUMY, NT7, NT8, NDAT, NS1, NS2, NOWD
      COMMON /GEN/ ITOT, ISIZE, NOWT, NOWY, NDAYS, NRUNS, IATRIB(18), ATRIB(4)
      COMMON /CLEAN/ DUM(5), KL4
      COMMON /HOUSE/ NH(6), N1, N2, N3, N4, N5, N6
      COMMON /RPT5/ IAAS, IAAE, IAAN, IAAFN, IAAM, IAAB, IAAFR

C PRINT INVENTORY LOCATIONS FROM ISTART TO ISTOP, DEBUGGING TOOL
      DIMENSION IN(3, 1), VEN(2, 1)

C CHECK ISTART=7777 IMPLIES ANIMAL ATTRIBUTE PRINTING BY BLD.
      IF(ISTART-7777) 70, 500, 70
70    ND1 = NT7

C MTP: GOT RID OF REFERENCES TO IDENT HERE, WASN'T NECESSARY, ANYWAY
C AND IT WAS JUST FOR PRESENTATION PURPOSES (ALTHOUGH THE FORMAT WASN'T DOCUMENTED)
      INN = 0
      IF(INN .EQ. 0) GO TO 900
      WRITE(ND1, 80) NOWT,(NH(I), I = 1,6), N6
80    FORMAT(' OUT,NOWT='I5'  NH(I)='6I3'  N6='I5)
      GO TO 12
900   WRITE(ND1, 90) NOWT, (NH(I), I = 1,6), N6
90    FORMAT(' OUT,NOWT='I5'  NH(I)='6I3'  N6='I5)
12    WRITE(ND1, 299)
299   FORMAT(2X'1---234--5---- 67-8----9----- 1011----12---- 1314----15---- 1---------.--- 2---------.---')
      DO 30 K = ISTART,ISTOP
30    WRITE(ND1, 300) (IN(I, K), I = 1,3), (VEN(II, K), II = 1,2), K
300   FORMAT(1X, 3I15, 15X, 2F15.3, I8)
      RETURN


C ANIMAL ATTRIBUTE PRINTING HOUSE BY HOUSE REQUEST.
500   IF(IAAS-NOWT) 502, 502, 899
502   IF(IAAE-NOWT) 899, 504, 504

C HEADING FOR NOWT DAILY ANIMAL ATTRIBUTES
504   NH2 = NH(2)
      NH4 = NH(4)
      NH3 = NH(3)
      NH5 = NH(5)
      NH1 = NH(1)
      WRITE(NT7, 5004) NOWT, NH2, NH4, NH3, NH5, NH1
5004  FORMAT(1H1,///,5X'DIAGNOSTIC REPORT OF ANIMAL ATTRIBUTES AT END OF DAY'I5/10X'INVENTORY IS- NURSERY='I4', FINISHING='I5', SOW MAINTENANCE='I4',  'I3' BOARS, AND FARROWING='I4)

C NURSERY WRITE ATTRIBUTES
      N777 = 0
      IF(IAAN) 700, 700, 550
550   IF(NH2) 700, 700, 552
552   WRITE(NT7, 5552)
5552  FORMAT(/5X'NURSERY BLD., SEX CLASS J=2 IS FEMALE, J=3 IS MALE,  SUBCLASS K=1 IS NEVER OVULATED.')
      WRITE(NT7, 5005)
5005  FORMAT(15X'HERD NO.=      AGE=    SEX J=    SUB.K=  DAY SIM.    BRTWT=      WT.=     GAIN= ')
      NI = N1 + 1
      NF = 5
      N77 = NH2
706   DO 559 I = 1,N77
      CALL REMOV(IN, VEN, NI)
      R11 = IATRIB(11)
      BRTWT = R11 / 100.0
      WRITE(NT7, 5559) IATRIB(5), IATRIB(1), IATRIB(2), IATRIB(3), IATRIB(9), BRTWT, ATRIB(1), ATRIB(2)
5559  FORMAT(12X, 5I10, 3F10.2)
      CALL FILEM(IN, VEN, NF)
559   CONTINUE
      IF(N777) 700, 700, 600

C FINISHING HOUSE WRITE ATTRIBUTES
700   IF(IAAFN) 600, 600, 702
702   IF(NH4) 600, 600, 704
704   WRITE(NT7, 5704)
5704  FORMAT(/5X'FINISHING BLD., SEX CLASS J=2 IS FEMALE, J=3 IS MALE,  SUBCLASS K=1 IS NEVER OVULATED.')
      WRITE(NT7, 5005)

C USE NURSERY LCCP ABOVE, GO TO MAINTENANCE AFTER
      NI = N3 + 1
      NF = 7
      N77 = NH4
      N777 = 1
      GO TO 706
600   IF(IAAM) 800, 800, 602
602   IF(NH3) 6900, 6900, 604
604   WRITE(NT7, 5604)
5604  FORMAT(/5X'MAINTENANCE BLD., SEX CLASSJ=2 IS FEMALE, SUBCLASS K=1 IS NEVER OVULATED, K=2 IS'/8X'OVULATING OPEN, K=3 IS GESTATION, K=4 IS LACTATION,  LTAL= NO. DAYS IN ABOVE SUBCLASS,'/8X'PIGS= NO. OF PIGS AT BIRTH, LEST= NO. DAYS IN ESTRUS CYCLE,  LGST= NO. DAYS FOR THIS GESTATION')
      WRITE(NT7, 5606)
5606  FORMAT(7X'HERD NO.=     AGE=   SEX J=   SUB.K=    LTAL=    PIGS=    LEST=    LGST= DAY SIM.     WT.=    GAIN=')
      DO 659 I = 1,NH3
      CALL REMOV(IN, VEN, N2 + 1)
      WRITE(NT7, 5669) IATRIB(5), IATRIB(1), IATRIB(2), IATRIB(3), IATRIB(4), IATRIB(8), IATRIB(10), IATRIB(11), IATRIB(9), ATRIB(1), ATRIB(2)
5669  FORMAT(5X, 9I9, 2F9.2)
      CALL FILEM(IN, VEN, 6)
659   CONTINUE
6900  NH6 = NH(6)
      WRITE(NT7, 6905) NH6
6905  FORMAT(5X'CULL PEN HAS'I5' SOWS FOR SALE IN ADDITION TO THOSE IN MAINT. BLD')
      IF(NH6) 800, 800, 6958
6958  DO 6959 I= 1, NH6
      CALL REMOV(IN, VEN, N5 + 1)
      WRITE(NT7,5669) IATRIB(5), IATRIB(1), IATRIB(2), IATRIB(3), IATRIB(4), IATRIB(8), IATRIB(10), IATRIB(11), IATRIB(9), ATRIB(1), ATRIB(2)
      CALL FILEM(IN, VEN, 9)
6959  CONTINUE
800   IF(IAAB) 400, 400, 802
802   IF(NH5) 400, 400, 804
804   WRITE(NT7, 5804)
5804  FORMAT(/5X'BOARS, SEX CLASS J=1 WITH SUBCLASS K=1 FOR NEW, K=2 FOR YOUNG, K=2 FOR MATURE,  LTAL=DAYS IN'/8X'SUBCLASS K   WEEK=6 DIGIT USE WITH RIGHTMOST RECENT DAY, ETC., SERVS= TOTAL SERVICES TO DATE')
      WRITE(NT7, 5806)
5806  FORMAT(7X'HERD NO.=     AGE=   SEX J=   SUB.K=    LTAL=    WEEK=   SERVS= DAY SIM.=    WT.=    GAIN')
      DO 859 I = 1,NH5
      CALL REMOV(IN, VEN, N4 + 1)
      WRITE(NT7, 5889) IATRIB(5), IATRIB(1), IATRIB(2), IATRIB(3), IATRIB(4), IATRIB(11), IATRIB(12), IATRIB(9), ATRIB(1), ATRIB(2)
5889  FORMAT(5X, 8I9, 2F9.2)
      CALL FILEM(IN, VEN, 8)
859   CONTINUE
400   IF(IAAFR) 788, 788, 402
402   IF(NH1) 788, 788, 404
404   WRITE(NT7, 5404)
5404  FORMAT(/5X'FARROWING BLD., SEX CLASS J=2 FOR FEMALE, J=3 IS MALE, SUBCLASS K=4 FOR SOWS IN LACTATION,'/8X'K=1 OR 0 FOR BABY PIGS, LTAL= NO. DAYS IN SUBCLASS K FOR SOW BUT=0 FORM PIGS, PIGS/DAM='/8X'NO. PIGS AT BIRTH FOR SOW, BUT HERD NO. OF DAM IF A PIG.  BRTHWT= WT. AT BIRTH,=0 FOR SOWS')
      WRITE(NT7, 5406)
5406  FORMAT(7X'HERD NO.=     AGE=   SEX J=   SUB.K=    LTAL= PIGS/DAM DAY SIM.= BRTHWT=   WT.=    GAIN=')
      DO 459 I = 1,NH1
      CALL REMOV(IN, VEN, 1)
      R11 = IATRIB(11)
      BRW = R11 / 100.0
      WRITE(NT7,5449) IATRIB(5), IATRIB(1), IATRIB(2), IATRIB(3), IATRIB(4), IATRIB(8), IATRIB(9), BRW, ATRIB(1), ATRIB(2)
5449  FORMAT(5X, 7I9, 3F9.2)
      CALL FILEM(IN, VEN, 4)
459   CONTINUE
788   WRITE(NT7, 6788)
6788  FORMAT(5X'END ANIMAL ATTRIBUTES DIAGNOSTIC REPORT FROM SUBROUTINE OUT'//)

899   RETURN
      END