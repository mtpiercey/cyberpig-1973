      SUBROUTINE REMOV(IN, VEN, KC)
            
      COMMON /INIT/ NR, NW, ND, NT7, NT8, NDAT, NS1, NS2
      COMMON /GEN/ ITOT, ISIZE, NOWT, NOWY, NDAYS, NRUNS, IATRIB(18), ATRIB(4)
      COMMON /HOUSE/ NH(6), N1, N2, N3, N4, N5, N6

      DIMENSION IN(3, 1), VEN(2, 1)

      KOL = KC

C REMOVES FILE COLUMN KOL, DECREMENT NH(KOL) BY ONE.
C   ATTRIBUTES PUT INTO IATRIB(), AND ATRIB().
C   ITOT (TOTAL ENTRIES IN FILE) DECREMENTED.

      IF(KOL) 88, 88, 200
200   IF(KOL-ITOT) 333, 333, 88
333   IF(KOL-N1) 1, 1, 20
1     KF = 1
      GO TO 555
20    IF(KOL - N2) 2, 2, 30
2     KF = 2
      GO TO 555
30    IF(KOL - N3) 3, 3, 40
3     KF = 3
      GO TO 555
40    IF(KOL - N4) 4, 4, 50
4     KF = 4
      GO TO 555
50    IF(KOL - N5) 5, 5, 60
5     KF = 5
      GO TO 555
60    IF(KOL - N6) 6, 6, 88
6     KF = 6
C MTP: HAD SOME INTEGER SIZE ISSUES HERE UNTIL I DID SOME MULTIPLICATION
C 555   IATRIB(1)=IN(1,KOL)/10000000000
555   IATRIB(1) = IN(1, KOL) / (1000000000 * 10)
      IATRIB(2) = MOD(IN(1, KOL) / 1000000000, 10)
      IATRIB(3) = MOD(IN(1, KOL) / 100000000, 10)
      IATRIB(4) = MOD(IN(1, KOL) / 100000, 1000)
      IATRIB(5) = MOD(IN(1, KOL), 100000)

C WORD NO. 2
      IATRIB(6) = IN(2, KOL) / (1000000000 * 10000)
      IATRIB(7) = MOD(IN(2, KOL) / (1000000000 * 100), 100)
      IATRIB(8) = MOD(IN(2, KOL) / 1000000, 100000)
      IATRIB(9) = MOD(IN(2, KOL), 1000000)

C WORD NO. 3
      IATRIB(10) = IN(3, KOL) / (1000000000 * 1000)
      IATRIB(11) = MOD(IN(3, KOL) / 1000000, 1000000)
      IATRIB(12) = MOD(IN(3, KOL), 1000000)

C ATRIB()
      ATRIB(1) = VEN(1, KOL)
      ATRIB(2) = VEN(2, KOL)

C PUSH DOWN FILES, BRING /HOUSE/ COMMON UP TO DATE
      DO 300 K = KOL,ITOT

      DO 302 I = 1,4
302   IN(I, K) = IN(I, K+1)

      DO 304 II = 1,2
304   VEN(II, K) = VEN(II, K+1)

300   CONTINUE
      NH(KF) = NH(KF) - 1
      ITOT = ITOT - 1
      N1 = NH(1)
      N2 = N1 + NH(2)
      N3 = N2 + NH(3)
      N4 = N3 + NH(4)
      N5 = N4 + NH(5)
      N6 = N5 + NH(6)
      RETURN
88    WRITE(ND, 87) KOL, NOWT
87    FORMAT('ERROR REMOV, KOL REQUESTED='I4'  ,NOWT='I8)
      RETURN
      END
