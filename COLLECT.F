      SUBROUTINE COLLECT(COLLECTOR, INDEX, X)

      DIMENSION COLLECTOR(5, 1)
      INTEGER INDEX
      REAL X

      COLLECTOR(1, INDEX) = COLLECTOR(1, INDEX) + X
      COLLECTOR(2, INDEX) = COLLECTOR(2, INDEX) + X * X
      COLLECTOR(3, INDEX) = COLLECTOR(3, INDEX) + 1.0
      COLLECTOR(4, INDEX) = AMIN1(COLLECTOR(4, INDEX), X)
      COLLECTOR(5, INDEX) = AMAX1(COLLECTOR(5, INDEX), X)

      RETURN
      END
